package com.fresearch.oversign.data;

public class ForumSearchObj {
	private String forumid;
	private String forumtitle;
	private String numberofcomment;
	private String numberofeye;
	private String userfullname;
	private String forumcreateddate;
	
	public String getForumID() {
		return forumid;
	}
	public void setForumID(String forumid) {
		this.forumid = forumid;
	}
	public String getForumTitle() {
		return forumtitle;
	}
	public void setForumTitle(String forumtitle) {
		this.forumtitle = forumtitle;
	}
	public String getNumberOfComment() {
		return numberofcomment;
	}
	public void setNumberOfComment(String numberofcomment) {
		this.numberofcomment = numberofcomment;
	}
	public String getNumberOfEye() {
		return numberofeye;
	}
	public void setNumberOfEye(String numberofeye) {
		this.numberofeye = numberofeye;
	}
	public String getUserFullName() {
		return userfullname;
	}
	public void setUserFullName(String userfullname) {
		this.userfullname = userfullname;
	}
	public String getForumCreatedDate() {
		return forumcreateddate;
	}
	public void setForumCreatedDate(String forumcreateddate) {
		this.forumcreateddate = forumcreateddate;
	}
}